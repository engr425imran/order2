    {
        
        Pay::create([
            'name' => $request->name,
            'type'=>$request->paycycletype,
            'firstPeriodEndDate' =>$request->firstPeriodEndDate,
            'lastDayMonth'=> $request->lastDayMonth,
            'employee_id'=> $request->employee,
            
        ]);
        return redirect('pays');
    }

